public class House extends Entity
{
	public House(Main app, int x, int y)
	{
        Box b = new Box(12, 12, 12); // create cube shape
        
        Geometry geom = new Geometry("Box", b);  // create cube geometry from the shape
        geom.setLocalTranslation(y, 250,x);
        Material mat = new Material(app.getAssetManager(),"Common/MatDefs/Misc/Unshaded.j3md");  // create a simple material
        mat.setColor("Color", ColorRGBA.Blue);   // set color of material to blue
        geom.setMaterial(mat); 
        app.getRootNode().attachChild(geom);
	}
}
